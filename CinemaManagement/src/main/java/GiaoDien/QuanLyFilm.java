    private void displayTable() {
        tableModel.setRowCount(0);
        List<Film> list = FilmDAO.getInstance().GetListFilm();
        for (int i = 0; i < list.size(); i++) {
            Film film = list.get(i);
            Object[] dt = {i + 1, film.getName(), film.getPrice(),film.getDramatic_name(), film.getStartDate(), film.getEndDate()};
            tableModel.addRow(dt);
        }
    }

