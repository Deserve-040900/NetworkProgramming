    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed

        // TODO add your handling code here:
        if (idSave >= 0) {
            Connection con = DBUtility.openConnection();
            try {
                PreparedStatement pstmt = con.prepareStatement("update film set name=?, price=?, dramatic_name=?, start_date=?, end_date=? where ID=?");
                pstmt.setString(1, txtfilmName.getText());
                pstmt.setString(2, txtPrice.getText());
                pstmt.setString(3, txtTheLoai.getText());
                pstmt.setString(4, txtdateStart.getText());
                pstmt.setString(5, txtdateEnd.getText());
                pstmt.setInt(6, idSave);
                int i = pstmt.executeUpdate();
                if (i > 0) {
                    displayTable();
                    JOptionPane.showMessageDialog(null, "Update Succesful!!");
                } else {
                    JOptionPane.showMessageDialog(null, "Update fail!!");
                }
            } catch (SQLException ex) {
                Logger.getLogger(QuanLyFilm.class.getName()).log(Level.SEVERE, null, ex);
            }
        } else {
            JOptionPane.showMessageDialog(null, "Seletet ID to update");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

