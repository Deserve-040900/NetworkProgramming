    private void displayTable() {
        tableModel.setRowCount(0);
        List<Opera> listTable = OperaDAO.getInstance().LoadListTables();

        for (int i = 0; i < listTable.size(); i++) {
            Opera tables = listTable.get(i);
            Object[] dt = {i + 1, tables.getOpera_Name(), tables.getTableStatus(), tables.getTableNote()};
            tableModel.addRow(dt);
        }
    }

