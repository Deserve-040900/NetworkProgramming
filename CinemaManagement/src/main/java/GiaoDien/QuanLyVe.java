    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
            // TODO add your handling code here:
            int invoiceId = InvoicesDAO.getInstance().GetUncheckInvoiceByTableId(idfilm);
            Film film = (Film) cbofilm.getSelectedItem();

            if (invoiceId == -1) {
                    InvoicesDAO.getInstance().Insert(idfilm, AccountDAO.getInstance().GetAccount().getId());
                    OrdersDAO.getInstance().Insert(film.getId(), InvoicesDAO.getInstance().GetMaxIdInvoice(), Integer.parseInt(spnAmount.getValue().toString()));
            } else {
                    OrdersDAO.getInstance().Insert(film.getId(), invoiceId, Integer.parseInt(spnAmount.getValue().toString()));
            }
            Connection con = DBUtility.openConnection();
            PreparedStatement pstmt;
            try {
                    pstmt = con.prepareStatement("update opera set status=1 where ID=?");
                    pstmt.setInt(1, idfilm);
                    pstmt.executeUpdate();
                    displayOpera();
                    displayTableFilm();
                    btnPay.setEnabled(true);
                    cbofilm.setSelectedIndex(0);
            } catch (SQLException ex) {
                    Logger.getLogger(QuanLyVe.class.getName()).log(Level.SEVERE, null, ex);
            }
    }//GEN-LAST:event_btnAddActionPerformed

