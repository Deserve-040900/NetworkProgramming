        private void displayOpera() {
                tableModelOpera.setRowCount(0);
                List<Opera> listTable = OperaDAO.getInstance().LoadListTables();
                for (int i = 0; i < listTable.size(); i++) {
                        Opera opera = listTable.get(i);
                        Object[] dt = {i + 1, opera.getOpera_Name(), opera.getTableStatus(), opera.getTableNote()};
                        tableModelOpera.addRow(dt);
                }
        }

        private void displayTableFilm() {
                tableModelFilm.setRowCount(0);

                List<MenuFilm> listMenu = MenuFilmDAO.getInstance().GetListMenuByTableId(idfilm);
                for (int i = 0; i < listMenu.size(); i++) {
                        MenuFilm menu = listMenu.get(i);
                        Object[] dt = {i + 1, menu.getFilm_Name(), menu.getCount(), menu.getPrice(), menu.getTotalPrice()};
                        totalPrice += menu.getTotalPrice();
                        tableModelFilm.addRow(dt);
                }
                txtTotalPrice.setText(totalPrice + "");
        }

        private void loadFilm() {
                List<Film> loadFilm = FilmDAO.getInstance().GetListFilm();
                for (Film film : loadFilm) {
                        comboBoxModel.addElement(film);
                }
        }
