    private void btnSignupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSignupActionPerformed
        // TODO add your handling code here:
//        int dk = JOptionPane.showConfirmDialog(this, "Do you want to SignUp ?", "Confirm", JOptionPane.YES_NO_OPTION);
//        if(dk != JOptionPane.YES_OPTION){   return;    }
//        try {
//            if(txtUsername.getText().equals("")|txtEmail.getText().equals("")|txtPassword.getText().equals("")|txtConfirm.getText().equals("")){}
//        } catch (Exception e) {}
        try {
            Registry r = LocateRegistry.getRegistry("localhost", 2000);
            RegisterModify c = (RegisterModify) r.lookup("RegisterModify");

            String username = txtUsername.getText();
            String email = txtEmail.getText();
            String password = new String(txtPassword.getPassword());
            String confirm = new String(txtConfirm.getPassword());

            if (username != null && email != null && password != null && confirm != null) {
                if (password.equals(confirm)) {
                    Register Re = new Register(username, email, password, confirm);
                    c.insert(Re);
                    int box = JOptionPane.showConfirmDialog(this, "Do you want to SignUp ?");
                    System.out.println(box);
                    if (box == JOptionPane.YES_OPTION) {
                        JOptionPane.showMessageDialog(null, "Sign Up Success");
                    } else {
                        return;
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "Please confirm your password again");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Invalid registration !!!");
            }
        } catch (HeadlessException | NotBoundException | RemoteException e) {
            java.util.logging.Logger.getLogger(RegisterGUI.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_btnSignupActionPerformed

