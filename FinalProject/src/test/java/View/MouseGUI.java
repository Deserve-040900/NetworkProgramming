    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        try {
            Registry r = LocateRegistry.getRegistry("localhost", 2000);
            MouseModify c = (MouseModify) r.lookup("MouseModify");
            String input = JOptionPane.showInputDialog(this, "Enter Mouse name to search");
            if (input != null && input.length() > 0) {
                mou = c.searchByName(input);
                tbm.setRowCount(0);

                mou.forEach(mouse -> {
                    tbm.addRow(new Object[]{
                        tbm.getRowCount() + 1, mouse.getName(), mouse.getPrice()
                    });
                });
            } else {
                showMouse();
            }
        } catch (HeadlessException | NotBoundException | RemoteException e) {
            java.util.logging.Logger.getLogger(MouseGUI.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_btnSearchActionPerformed


