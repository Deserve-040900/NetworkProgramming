    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        try {
            Registry r = LocateRegistry.getRegistry("localhost", 2000);
            MouseModify c = (MouseModify) r.lookup("MouseModify");
            int selectedIndex = tbMouse.getSelectedRow();
            if (selectedIndex >= 0) {
                Mouse cpt = mou.get(selectedIndex);
                // show box option
                int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this object ?");
                System.out.println("Option: " + option);
                // 0 yes 1 no 2 cancel
                if (option == 0) {
                    c.delete(cpt.getId());
                    showMouse();
                } else {
                    showMouse();
                }
            }
        } catch (HeadlessException | NotBoundException | RemoteException e) {
            java.util.logging.Logger.getLogger(MouseGUI.class.getName()).log(java.util.logging.Level.SEVERE, null, e);
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

