    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        String name = txtName.getText();
        String color = txtColor.getText();
        int price = Integer.parseInt(txtPrice.getText());
        
        Keyboard Key = new Keyboard(name, color, price);
        KeyboardCRUB.insert(Key);
        
        showKeyboard();
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedIndex = tbKeyboard.getSelectedRow();
        if(selectedIndex >= 0){
            Keyboard cpt = key.get(selectedIndex);
            // show box option
            int option = JOptionPane.showConfirmDialog(this, "Do you want to delete this object ?");
            System.out.println("Option: " + option);
            // 0 yes 1 no 2 cancel
            if(option == 0){
                KeyboardCRUB.delete(cpt.getId());
                showKeyboard();
            }else{
                showKeyboard();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // TODO add your handling code here:
        String input = JOptionPane.showInputDialog(this, "Enter Keyboard name to search");
        if(input != null && input.length() > 0){
            key = KeyboardCRUB.searchByName(input);
            tbm.setRowCount(0);
            
            key.forEach(keyboard -> {
                tbm.addRow(new Object[] {
                    tbm.getRowCount() + 1, keyboard.getName(), keyboard.getColor(), keyboard.getPrice()
                });
            });
        }else{
            showKeyboard();
        }
    }//GEN-LAST:event_btnSearchActionPerformed


