    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        // lấy thông tin của user
        String username = txtName.getText();
        String password = new String(txtPass.getPassword());
        String path = txtPath.getText();
        int per;
        
        if(rabtnRead.isSelected()){
            per = 0;
        }else if(rabtnRead.isSelected()){
            per = 1;
        }else{
            per = 2;
        }
        
        // tạo kết nối và thêm user vào CSDL
        DB_Access ac = new DB_Access();
        int kq = ac.Update("Insert into account values('" + username + "','" + password + "','" + path + "','" + per + ")");
        // kiểm tra đăng ký
        if(kq!=0){
            JOptionPane.showMessageDialog(null, "Register success.");
        }else{
            JOptionPane.showMessageDialog(null, "Register fail!");
        }
    }//GEN-LAST:event_btnRegisterActionPerformed

